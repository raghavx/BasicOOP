package com.raghavx;

public class Main {
	/**
	 * Entry Point of the Java Program
	 * 1. Object 
	 * 		
	 * 2. Class
	 * 		
	 * 3. Inheritance 
	 * 		
	 * 4. Interface 
	 * 		
	 * 5. Package 
	 * 
	 * 
	 * @param args
	 */
	public static void main(String[] args) {

		/*
		 * Primitive 
		 * 
		 */
		// integer data type 
		byte b = 0; // -128 to 127 
		System.out.println(b);
		short s = 0; // -32,768 to 32,767
		System.out.println(s);
		int i = 0; // -2^31 to 2^31 - 1
		System.out.println(i);
		long l = 0l;  // -2^63 to 2^63 - 1
		System.out.println(l);
		// decimal / floating point 
		float f = 0.0f; // single precision 32 bit 
		System.out.println(f);
		double d = 0.0;// double  precision 64 bit 
		System.out.println(d);
		
		// 
		boolean bt = false; // 1 bit 
		System.out.println(bt);
		//
		char c = 'A', cc = 'B'; // 16 bit unicode 
		
		System.out.println(cc);
		System.out.println();
		System.out.println(Short.MIN_VALUE);
		System.out.println(Integer.MIN_VALUE);
		System.out.println(Long.MIN_VALUE);
		System.out.println(Float.MIN_VALUE);
		System.out.println(Double.MIN_VALUE);
		
		// print the max values 
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
